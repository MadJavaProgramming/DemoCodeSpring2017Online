/** Represents a one item on a menu
 * 
 *@author pwaite
 */
 
 public class MenuItem {
 
     private String name;
	
     /**
	 * Returns the value of name.
	 * @return the name 
	 */
	public String getName() {
		return name;
	}


	/**
	 * Sets the value of name.
	 * @param name The value to assign name.
	 */
	public void setName(String name) {
		this.name = name;
	}

 }
