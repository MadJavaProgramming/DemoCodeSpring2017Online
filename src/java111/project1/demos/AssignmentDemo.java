/** Demonstration of creating variables and assigning values to those variables
  *
  * @author pwaite
  **/
  public class AssignmentDemo {
  
      /** The main method to run our program
       *  create a couple variables, assign values, and display the variables
       **/
       public static void main(String[] args) {
           // create a variable to hold a number
           int myNumber = 55;
           
           // display the myNumber variable
           System.out.println("myNumber is: " + myNumber);
           
           // display a blank line
           System.out.println();
           
           // create a variable to hold a String
           String myString = "This is some text";
           
           // display the myString variable
           System.out.println("myString is: " + myString);
       }
   }  
